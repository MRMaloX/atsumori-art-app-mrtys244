const museumItems = [
    ...artworks,
    ...sculptures
];

const artGrid = document.getElementById("art-grid");
const sculptureGrid = document.getElementById("sculpture-grid");

const modal = document.getElementById("modal");
const modalContent = document.getElementById("modal-content");

let currentFilter = "all";

loadOwnedData();

function saveOwnedData() {

    const ownedData = museumItems.map(item => item.owned);

    localStorage.setItem(
        "atsumori_art_owned",
        JSON.stringify(ownedData)
    );
}

function loadOwnedData() {

    const savedData =
        localStorage.getItem("atsumori_art_owned");

    if (!savedData) {

        return;
    }

    const ownedData =
        JSON.parse(savedData);

    museumItems.forEach((item, index) => {

    if (ownedData[index] !== undefined) {

        item.owned = ownedData[index];
    }
});
}

function updateProgress() {

    const ownedCount =
    museumItems.filter(item => item.owned).length;

    const remaining =
    museumItems.length - ownedCount;

    document.getElementById("progress-count").textContent =
        `収集率 ${ownedCount} / ${museumItems.length}`;

    const remainingText =
        document.getElementById("remaining-count");

    if (remaining === 0) {

        remainingText.textContent =
            "コンプリート達成！";

        remainingText.classList.add("complete");
    }

    else {

        remainingText.textContent =
            `コンプリートまであと${remaining}個`;

        remainingText.classList.remove("complete");
    }
}

function openModal(art) {

    if (art.fake) {

        modalContent.innerHTML = `
            <div class="compare-area">

                <div class="compare-block">
                    <div class="compare-label real-label">本物</div>
                    <img src="${art.image}">
                </div>

                <div class="compare-block">
                    <div class="compare-label">偽物</div>
                    <img src="${art.fake}">
                </div>

            </div>

            <div class="description-text">
                ${art.description || "特徴情報なし"}
            </div>
        `;
    }

    else {

        modalContent.innerHTML = `
            <div class="no-fake">
                偽物は存在しません
            </div>
        `;
    }

    modal.classList.remove("hidden");
}

function closeModal() {

    modal.classList.add("hidden");
}

function renderArtworks() {

    renderSection(
        artworks,
        artGrid
    );

    renderSection(
        sculptures,
        sculptureGrid
    );
}

function renderSection(items, targetGrid) {

    targetGrid.innerHTML = "";

    items
        .filter((item) => {

            if (currentFilter === "owned") {
                return item.owned;
            }

            if (currentFilter === "unowned") {
                return !item.owned;
            }

            return true;
        })
        .forEach((item) => {

            const card =
                document.createElement("div");

            card.className = "art-card";

            card.innerHTML = `
                <img src="${item.image}">

                <div class="art-name">
                    ${item.name}
                </div>

                <div class="checkbox-area">
                    <label>
                        <input type="checkbox"
                        ${item.owned ? "checked" : ""}>
                        <span>所持</span>
                    </label>
                </div>

                <button class="check-btn">
                    本物チェック
                </button>
            `;

            const checkbox =
                card.querySelector("input");

            checkbox.addEventListener("change", () => {

                item.owned = checkbox.checked;

                saveOwnedData();

                updateProgress();

                renderArtworks();
            });

            card
                .querySelector(".check-btn")
                .addEventListener("click", () => {

                    openModal(item);
                });

            targetGrid.appendChild(card);
        });
}

document
    .getElementById("show-all")
    .addEventListener("click", () => {

        currentFilter = "all";

        renderArtworks();
    });

document
    .getElementById("show-owned")
    .addEventListener("click", () => {

        currentFilter = "owned";

        renderArtworks();
    });

document
    .getElementById("show-unowned")
    .addEventListener("click", () => {

        currentFilter = "unowned";

        renderArtworks();
    });

document
    .getElementById("modal-close")
    .addEventListener("click", closeModal);

document
    .getElementById("modal-bg")
    .addEventListener("click", closeModal);

renderArtworks();

